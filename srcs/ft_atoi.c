#include "ft.h"

int	ft_atoi(const char *str)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		c = c * 10 + str[i] - '0';
		i++;
	}
	return (c);
}
